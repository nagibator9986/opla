class Command(BaseCommand):
    help = "Создаёт стартовый набор из 12 параметров аудита (idempotent)."

    def add_arguments(self, parser):
        parser.add_argument(
            "--overwrite",
            action="store_true",
            help="Перезаписать system_prompt у существующих параметров (DESTRUCTIVE).",
        )

    def handle(self, *args, overwrite: bool = False, **options):
        created = 0
        updated = 0
        skipped = 0
        for data in PARAMETERS:
            obj, was_created = AuditParameter.objects.get_or_create(
                code=data["code"],
                defaults={
                    "name": data["name"],
                    "description": data["description"],
                    "system_prompt": data["system_prompt"],
                    "order": data["order"],
                    "is_active": True,
                },
            )
            if was_created:
                created += 1
                continue
            if overwrite:
                obj.name = data["name"]
                obj.description = data["description"]
                obj.system_prompt = data["system_prompt"]
                obj.order = data["order"]
                obj.save(update_fields=["name", "description", "system_prompt", "order", "updated_at"])
                updated += 1
            else:
                skipped += 1
        self.stdout.write(
            self.style.SUCCESS(
                f"AuditParameters: created={created}, updated={updated}, skipped={skipped}"
            )
        )
