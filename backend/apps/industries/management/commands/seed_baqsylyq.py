class Command(BaseCommand):
    help = "Seed / re-seed the Digital Baqsylyq adaptive questionnaire."

    def add_arguments(self, parser):
        parser.add_argument(
            "--fresh",
            action="store_true",
            help="Remove the existing active Baqsylyq template first (DESTRUCTIVE).",
        )

    @transaction.atomic
    def handle(self, *args, fresh: bool = False, **options):
        industry, _ = Industry.objects.get_or_create(
            code=INDUSTRY_DATA["code"],
            defaults={
                "name": INDUSTRY_DATA["name"],
                "description": INDUSTRY_DATA["description"],
                "is_active": True,
            },
        )

        existing = QuestionnaireTemplate.objects.filter(
            industry=industry, is_active=True
        ).first()

        if existing and not fresh:
            self.stdout.write(
                self.style.WARNING(
                    f"Active Baqsylyq template already exists ({existing}). "
                    "Run with --fresh to overwrite."
                )
            )
            return

        if existing and fresh:
            existing.is_active = False
            existing.save(update_fields=["is_active"])
            version = existing.version + 1
        else:
            version = 1

        template = QuestionnaireTemplate.objects.create(
            industry=industry,
            version=version,
            is_active=True,
            name="Digital Baqsylyq",
            intro_text=INTRO,
            completion_text=COMPLETION,
        )

        order = 0
        questions_by_key: dict[str, Question] = {}

        def add(stage: str, text: str, field_type: str, **extra) -> Question:
            nonlocal order
            order += 1
            return Question.objects.create(
                template=template,
                order=order,
                stage=stage,
                text=text,
                field_type=field_type,
                **extra,
            )

        # ─── Stage I: Паспорт компании ─────────────────────────────────────
        for text, ftype, ph in PASSPORT:
            add(STAGE_I, text, ftype, placeholder=ph, block=Question.Block.A)

        # ─── Stage II: Роль (триггер) ──────────────────────────────────────
        role_q = add(
            STAGE_II,
            ROLE_CHOICE,
            Question.FieldType.CHOICE,
            options={"choices": [ROLE_OWNER, ROLE_TOP, ROLE_MANAGER]},
            block=Question.Block.A,
        )
        questions_by_key["role"] = role_q

        # ─── Stage III: Идентификация — только для Владельцев + Топов ──────
        for text, ftype, ph in IDENTIFICATION:
            add(
                STAGE_III,
                text,
                ftype,
                placeholder=ph,
                block=Question.Block.B,
                condition_question=role_q,
                condition_values=[ROLE_OWNER, ROLE_TOP],
            )

        # ─── Block II: Менеджер ──────────────────────────────────────────
        for text in MANAGER_QUESTIONS:
            add(
                BLOCK_MANAGER,
                text,
                Question.FieldType.LONGTEXT,
                block=Question.Block.C,
                condition_question=role_q,
                condition_values=[ROLE_MANAGER],
            )

        # ─── Block II: Владелец / Топ ────────────────────────────────────
        for text in OWNER_QUESTIONS:
            add(
                BLOCK_OWNER,
                text,
                Question.FieldType.LONGTEXT,
                block=Question.Block.C,
                condition_question=role_q,
                condition_values=[ROLE_OWNER, ROLE_TOP],
            )

        self.stdout.write(
            self.style.SUCCESS(
                f"Created Digital Baqsylyq v{version}: {template.questions.count()} questions"
            )
        )
