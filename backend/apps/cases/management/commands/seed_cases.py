class Command(BaseCommand):
    help = "Create sample Case rows (idempotent — skip if slug exists)."

    def handle(self, *args, **options):
        created = 0
        skipped = 0
        for data in CASES:
            slug = data["slug"]
            if Case.objects.filter(slug=slug).exists():
                skipped += 1
                continue
            Case.objects.create(**data)
            created += 1
        self.stdout.write(
            self.style.SUCCESS(f"Cases: created={created}, skipped={skipped}")
        )
